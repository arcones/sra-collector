CREATE TABLE sracollector.GEO_EXPERIMENT
(
    ID         SERIAL PRIMARY KEY,
    NCBI_ID    INTEGER     NOT NULL,
    REQUEST_ID VARCHAR     NOT NULL REFERENCES sracollector.REQUEST (ID) ON UPDATE CASCADE ON DELETE CASCADE,
    GSM        VARCHAR(50) NOT NULL,
    UNIQUE (REQUEST_ID, GSM)
);

CREATE TABLE sracollector_dev.GEO_EXPERIMENT
(
    ID         SERIAL PRIMARY KEY,
    NCBI_ID    INTEGER     NOT NULL,
    REQUEST_ID VARCHAR     NOT NULL REFERENCES sracollector_dev.REQUEST (ID) ON UPDATE CASCADE ON DELETE CASCADE,
    GSM        VARCHAR(50) NOT NULL,
    UNIQUE (REQUEST_ID, GSM)
);
